def es_primo(num, n=2):
    if n >= num:
        return True
    elif num % n != 0:
        return es_primo(num, n + 1)
    else:
        return False

print("Los primeros 10 n√∫meros primos son:")

primos = []
num = 1
while len(primos) < 10:
    if esprimo(num):
        primos.append(num)
    num += 1

